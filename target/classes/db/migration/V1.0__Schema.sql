CREATE TABLE ROLES
(
    ID   INTEGER AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(250)
);

CREATE TABLE USERS
(
    ID             INTEGER AUTO_INCREMENT PRIMARY KEY,
    USER_TYPE      VARCHAR(250),
    FIRST_NAME     VARCHAR(250),
    LAST_NAME      VARCHAR(250),
    USERNAME       VARCHAR(250),
    PASSWORD       VARCHAR(250),
    ACTIVE_ACCOUNT BOOLEAN
);

CREATE TABLE USERS_ROLE
(
    ID      INTEGER AUTO_INCREMENT PRIMARY KEY,
    USER_ID INTEGER NOT NULL,
    ROLE_ID INTEGER NOT NULL,
    UNIQUE (USER_ID, ROLE_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ID)
);

CREATE TABLE EMPLOYEES
(
    ID      INTEGER AUTO_INCREMENT PRIMARY KEY,
    USER_ID INTEGER,
    UNIQUE (USER_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS (ID)
);

CREATE TABLE BUSINESSES
(
    ID   INTEGER AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(256)
);

CREATE TABLE ROBOT
(
    ID           INTEGER AUTO_INCREMENT PRIMARY KEY,
    MODEL        VARCHAR,
    TYPE         VARCHAR,
    POWER        VARCHAR,
    DRILL        VARCHAR,
    TABLE_NUMBER VARCHAR,
    HEIGHT       VARCHAR,
    TILT         VARCHAR,
    SIZE         VARCHAR,
    WEIGHT       VARCHAR,
    BIO          VARCHAR
);

CREATE TABLE ACTIONS
(
    ID   INTEGER AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(250)
);

CREATE TABLE PRODUCTS
(
    ID   BIGINT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(250)
);

CREATE TABLE MANUFACTURING_PROCESS
(
    ID           VARCHAR(50),
    PRODUCT_ID   INTEGER DEFAULT 0,
    ACTION_ID    INTEGER DEFAULT 0,
    ROBOT_ID     INTEGER DEFAULT 0,
    WORKING_TIME TIME,
    FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID),
    FOREIGN KEY (ACTION_ID) REFERENCES ACTIONS (ID),
    FOREIGN KEY (ROBOT_ID) REFERENCES ROBOT (ID)
);

CREATE TABLE BUSINESSES_EMPLOYEES
(
    ID          INTEGER AUTO_INCREMENT PRIMARY KEY,
    BUSINESS_ID INTEGER,
    EMPLOYEE_ID INTEGER,
    PRIMARY KEY (ID),
    FOREIGN KEY (BUSINESS_ID) REFERENCES BUSINESSES (id),
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (id)
);

CREATE TABLE PRODUCT_EMPLOYEES
(
    ID          INTEGER AUTO_INCREMENT PRIMARY KEY,
    EMPLOYEE_ID INTEGER,
    PRODUCT_ID  INTEGER,
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (id),
    FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID)
)